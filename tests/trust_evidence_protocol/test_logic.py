from trust_test_support import assert_case


def test_unknown_sasha_home_is_red():
    assert_case(
        """
        Факты:
        1. Алиса — девочка, Маша — девочка, Игорь — мальчик, Света — девочка, Толя — мальчик, Оксана — девочка, Михаил — мальчик, Саша — ?
        2. После школы девочки идут в кафе, а мальчики в кино.
        3. После кафе некоторые идут в театр, а некоторые сразу домой. После кино некоторые идут на рыбалку, а другие в палаточный лагерь.

        Тестовый кейс:
        Проверить, что Саша дома. Если да, тест зелёный, если нет, тест красный.

        Реши, можно ли делать этот тест зелёным.
        """,
        expected="red",
        answer_options=("green", "red"),
    )


